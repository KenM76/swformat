def test_weldment_dimele_user_records_roundtrip() -> None:
    """A v19000 weldment (multi-element records incl. moCusPropDimEle_c) parses
    and its user-record region re-serializes byte-exact.

    Guards the flag-as-COUNT + moCusPropDimEle_c (`cstr2_dim` + 22-byte trailer)
    decode. Scans a gitignored local samples tree for a part whose CusProps uses
    moCusPropDimEle_c; skips if none present (real CAD files are local-only and
    never committed). No third-party data is asserted, only structural round-trip.
    """
    from swformat.carchive import cusprops as cp

    samples = ROOT / "research" / "empirical_findings" / "twin_save_baseline" / "samples"
    target = None
    if samples.exists():
        for p in sorted(samples.rglob("*.SLDPRT")):
            if p.name.lower().startswith(("twina", "twinb")):
                continue
            try:
                cus = swformat.read_document(p).streams().get("Contents/CusProps")
            except Exception:
                continue
            if cus and b"moCusPropDimEle_c" in cus:
                target = (p, cus)
                break
    if target is None:
        pytest.skip("no staged weldment with moCusPropDimEle_c present")
    _p, cus = target
    n_props, first_off, _ = cp.parse_container_header(cus)
    recs, end_off = cp.walk_user_records(cus)
    assert len(recs) == n_props
    # the structural re-emit of the user-record region is byte-exact
    assert cp.serialize_user_records(cus) == cus[first_off:end_off]


def test_add_text_properties_byte_exact_vs_sw() -> None:
    """The CusProps writer reproduces SOLIDWORKS's own add-2-properties output.

    Ground truth: c_B0 (10 props) and c_B2 (c_B0 + ZZ_ONE/ZZ_TWO added by SW),
    both SW-saved to one fixed filename (no embedded-name artifact). Adding the
    same two text properties via our writer must yield c_B2 byte-for-byte.
    """
    base = ROOT / "research" / "empirical_findings" / "cusprops_carchive"
    b0p, b2p = base / "c_B0.SLDPRT", base / "c_B2.SLDPRT"
    if not (b0p.exists() and b2p.exists()):
        pytest.skip("CusProps diff-pair (c_B0/c_B2) not present")
    b0 = swformat.read_document(b0p).streams()["Contents/CusProps"]
    b2 = swformat.read_document(b2p).streams()["Contents/CusProps"]
    got = add_text_properties(b0, [("ZZ_ONE", "valuethefirst"), ("ZZ_TWO", "second_val")])
    assert got == b2, "writer output is not byte-identical to SOLIDWORKS's"


