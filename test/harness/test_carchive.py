def test_objmap_ledger_nested_index_accounting() -> None:
    """A body that serialized inline sub-objects advances the shared index by
    those nested objects too, so later top-level objects get correct indices."""
    import struct as _s

    from swformat.carchive.archive import TagKind, write_class_def, write_object_tag
    from swformat.carchive.objmap import serialize_objmap, walk_objmap

    # body convention for this test: <u16 len><u16 nested><len bytes>
    def body_len(data, offset, ctx):
        ln = _s.unpack_from("<H", data, offset)[0]
        nested = _s.unpack_from("<H", data, offset + 2)[0]
        return offset + 4 + ln, nested

    def body(payload: bytes, nested: int) -> bytes:
        return _s.pack("<HH", len(payload), nested) + payload

    stream = (
        # Outer: NEW_CLASS (class=1,obj=2); body declares 2 nested indices
        write_object_tag(TagKind.NEW_CLASS) + write_class_def(1, "Outer") + body(b"\xaa\xbb", 2)
        # Next: NEW_CLASS — must land on class=5,obj=6 (1 + 2 base + 2 nested = 5)
        + write_object_tag(TagKind.NEW_CLASS) + write_class_def(1, "Next") + body(b"\xcc", 0)
    )
    led = walk_objmap(stream, body_len)
    assert led.entries[0].class_index == 1 and led.entries[0].obj_index == 2
    assert led.entries[0].nested_indices == 2
    assert led.entries[1].class_index == 5 and led.entries[1].obj_index == 6
    assert serialize_objmap(led) == stream  # byte-exact despite nesting


# Header body sizes by class (from cusprops._object_map_walk): the CusProps
# container chain mgr -> container -> filecontainer -> suObList -> advlist.
_CUSPROPS_HEADER_BODY = {
    "moCusPropMgr_c": 16, "moCusPropContainer_c": 0, "moFilePropContainer_c": 0,
    "suObList": 2, "moAdvCusPropList_c": 2,
}


def _cusprops_header_oracle(data, offset, ctx):
    cn = ctx["class_name"]
    if cn not in _CUSPROPS_HEADER_BODY:
        raise AssertionError(f"unexpected header class {cn!r}")
    return offset + _CUSPROPS_HEADER_BODY[cn]


