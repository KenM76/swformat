def test_cstring_roundtrip() -> None:
    for s in ["", "REVISION", "docProps/custom.xml", "a & b", "ZZ_TEST", "héllo"]:
        enc = encode_cstring(s)
        got = read_cstring(enc, 0)
        assert got is not None
        text, nxt = got
        assert text == s and nxt == len(enc)
    # non-CString bytes return None
    assert read_cstring(b"\x00\x01\x02\x03", 0) is None


def test_cstring_roundtrip_astral() -> None:
    """Astral chars (surrogate pairs) round-trip: length is UTF-16 code units.

    Regression: encode_cstring used len(text) (code points), which writes too
    few bytes for the declared length when a code point is two UTF-16 code
    units, breaking the round-trip. Length must be len(enc)//2.
    """
    for s in ["\U00020bb7", "\U0001F600 part", "mix \U0001F4801 end"]:
        enc = encode_cstring(s)
        # declared length == UTF-16 code units, not Python str length
        assert enc[3] == len(s.encode("utf-16-le")) // 2
        got = read_cstring(enc, 0)
        assert got is not None
        text, nxt = got
        assert text == s and nxt == len(enc)


def test_object_tag_classify_and_roundtrip() -> None:
    assert read_object_tag(b"\x00\x00", 0) == (TagKind.NULL, 0, 2)
    assert read_object_tag(b"\xff\xff", 0) == (TagKind.NEW_CLASS, 0, 2)
    assert read_object_tag(b"\x0b\x80", 0) == (TagKind.CLASS_REF, 0x0b, 2)
    assert read_object_tag(b"\x05\x00", 0) == (TagKind.OBJECT_REF, 5, 2)
    assert write_object_tag(TagKind.NULL) == b"\x00\x00"
    assert write_object_tag(TagKind.NEW_CLASS) == b"\xff\xff"
    assert write_object_tag(TagKind.CLASS_REF, 0x0b) == b"\x0b\x80"
    assert write_object_tag(TagKind.OBJECT_REF, 5) == b"\x05\x00"


def test_class_def_roundtrip() -> None:
    blob = write_class_def(1, "moCusPropMgr_c")
    assert blob == b"\x01\x00\x0e\x00moCusPropMgr_c"
    schema, name, nxt = read_class_def(blob, 0)
    assert (schema, name, nxt) == (1, "moCusPropMgr_c", len(blob))


def test_scan_class_defs_finds_defs_in_order() -> None:
    """Forward-scan recovers each NEW_CLASS def (offset, schema, name) in file
    order, and is not fooled by a bare ``FF FF`` (NEW_CLASS tag) that is not
    followed by a valid class framing."""
    a = write_class_def(1, "moDrawing_c")
    b = write_class_def(1, "moLengthParameter_c")
    c = write_class_def(1, "moDisplayDistanceDim_c")
    # FF FF tags introduce each def; junk + a lone FF FF (no valid framing) between.
    blob = (b"\xff\xff" + a + b"\x00\x00body" +
            b"\xff\xff" + b +
            b"\xff\xff\x99\x99\x99\x99" +            # FF FF with implausible schema/name
            b"\xff\xff" + c + b"tail")
    defs = scan_class_defs(blob)
    assert [d.name for d in defs] == ["moDrawing_c", "moLengthParameter_c",
                                      "moDisplayDistanceDim_c"]
    assert all(isinstance(d, ClassDef) and d.schema == 1 for d in defs)
    # offsets strictly increasing (definition order) and point at the FF FF tag
    offs = [d.offset for d in defs]
    assert offs == sorted(offs)
    assert blob[offs[0]:offs[0] + 2] == b"\xff\xff"


def test_scan_class_defs_suffix_filter_and_empty() -> None:
    """The default accepts any identifier (so non-``_c`` classes like
    ``moDrSheet`` / ``sgSketch`` are found); an optional ``name_suffix`` filters;
    a stream with no class defs yields an empty list."""
    assert scan_class_defs(b"no class defs here, just text \xff\xff\x01\x00") == []
    # a non-_c identifier (like the real moDrSheet/sgSketch) IS found by default...
    blob = b"\xff\xff" + write_class_def(1, "moDrSheet")
    assert [d.name for d in scan_class_defs(blob)] == ["moDrSheet"]
    # ...but can be excluded with an explicit suffix filter.
    assert scan_class_defs(blob, name_suffix=b"_c") == []
    assert [d.name for d in scan_class_defs(
        b"\xff\xff" + write_class_def(1, "moThing_c"), name_suffix=b"_c"
    )] == ["moThing_c"]


