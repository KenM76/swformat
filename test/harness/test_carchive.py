@pytest.mark.layer2
@pytest.mark.skipif(not CORPUS, reason="no corpus file with Contents/CusProps")
@pytest.mark.parametrize(("tag", "path"), CORPUS, ids=_IDS)
def test_walk_user_records(tag: str, path: Path) -> None:
    """The record walker segments the property list exactly and matches the reader."""
    data = swformat.read_document(path).streams()["Contents/CusProps"]
    records, end_off = walk_user_records(data)
    n_props, first_off, _ = parse_container_header(data)

    # one record per property, in the same name->value mapping as the reader
    assert len(records) == n_props
    assert {r["name"]: r["value"] for r in records} == read_cusprops(data)
    if n_props == 0:
        return

    # records are contiguous and span exactly [first_off, end_off)
    assert records[0]["start"] == first_off
    for a, b in itertools.pairwise(records):
        assert a["end"] == b["start"]
    assert records[-1]["end"] == end_off

    # the walk lands on the suObList terminator 00 00 (CObList end of the list)
    assert data[end_off:end_off + 2] == b"\x00\x00"
    # for a doc with a cut list, the cutlist container NEW_CLASS follows it
    cut = data.find(b"moCutListPropContainer_c")
    if cut >= 0:
        kind, _, _ = read_object_tag(data, end_off + 2)
        assert kind is _TK.NEW_CLASS


