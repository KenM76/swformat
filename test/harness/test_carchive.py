def _corpus_with_cusprops() -> list[tuple[str, Path]]:
    cfg = ROOT / "test" / "corpus" / "corpus.config.json"
    if not cfg.exists():
        return []
    out = []
    for e in json.loads(cfg.read_text(encoding="utf-8")).get("files", []):
        p = Path(e["path"])
        if p.exists() and "Contents/CusProps" in swformat.read_document(p).streams():
            out.append((e["tag"], p))
    return out


