def test_container_header_parse(tag: str, path: Path) -> None:
    """The structural header parser agrees with the heuristic reader + lands clean."""
    from swformat.carchive.archive import read_object_tag

    cusprops = swformat.read_document(path).streams()["Contents/CusProps"]
    n_props, first_off, class_map = parse_container_header(cusprops)
