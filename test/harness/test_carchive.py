# Object-map ledger (M5.1 Step 2 — codec core). Self-contained: a hand-built
# CArchive stream with a length-prefixed body convention proves the index rule
# + byte-exact round-trip independent of any real per-class body parser.
# --------------------------------------------------------------------------- #

def _lp_body_len(data, offset, ctx):
    """Test oracle: each body is <u16 len><len bytes>."""
    import struct as _s
    ln = _s.unpack_from("<H", data, offset)[0]
    return offset + 2 + ln


def _lp_body(payload: bytes) -> bytes:
    import struct as _s
    return _s.pack("<H", len(payload)) + payload


def test_objmap_ledger_roundtrip_and_indices() -> None:
    from swformat.carchive.archive import (
        TagKind,
        write_class_def,
        write_object_tag,
    )
    from swformat.carchive.objmap import serialize_objmap, walk_objmap
    # Build: NEW_CLASS "Foo"(schema 1)+body, class-ref Foo + body, OBJECT_REF 2, NULL
    stream = (
        write_object_tag(TagKind.NEW_CLASS) + write_class_def(1, "Foo") + _lp_body(b"\xaa\xbb\xcc")
        + write_object_tag(TagKind.CLASS_REF, 1) + _lp_body(b"\xdd")
        + write_object_tag(TagKind.OBJECT_REF, 2)
        + write_object_tag(TagKind.NULL)
    )
    led = walk_objmap(stream, _lp_body_len)
    kinds = [e.kind for e in led.entries]
    assert kinds == [TagKind.NEW_CLASS, TagKind.CLASS_REF, TagKind.OBJECT_REF, TagKind.NULL]
    # MFC index rule: Foo class=1, first Foo obj=2, second Foo obj=3
    assert led.entries[0].class_index == 1 and led.entries[0].obj_index == 2
